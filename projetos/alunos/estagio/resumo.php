<h2 class="title">Resumo de um artigo científico</h2>

       
<p>O resumo deve conter uma descrição completa e concisa dos componentes-chave da metodologia do estudo e dos achados importantes da pesquisa.</p>
<br />

<p>Partes de um resumo:</p>
<ul>
<li><b>Contexto</b>. Fornecendo uma introdução ao problema de pesquisa.</li>
<li><b>Propósito</b>. Indicando hipótese ou objetivo de estudo.</li>
<li><b>Metodologia</b>. Como foi conduzido o estudo?</li>
<li><b>Resultados</b>. Quais foram os achados?</li>
<li><b>Conclusão</b>. O que significam os resultados?</li>
</ul>


<br />
<p>O resumo não deve conter nenhuma surpresa, deve resumir exatamente o que o leitor vai encontrar no texto.</p>
