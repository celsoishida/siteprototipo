<h4>Posso colocar um parágrafo de um outro autor exatamente como o original?</h4>
<p>A melhor maneira é colocar com suas palavras (você como uma pessoa que leu, entendeu o trabalho e escreve apenas a ideia que relaciona com o seu trabalho). Se mesmo assim, você ainda quiser colocar o texto exato, coloque no formato padrão ABNT de citação.</p>
<br /><h4>Posso citar um blog?</h4>
<p>No trabalho acadêmico e sob minha orientação: NUNCA.</p>
<h4>Posso citar wikipedia?</h4>
<p>O melhor é citar livros ou artigos científicos reconhecidos pela comunidade. Se você leu e conhece apenas do wikipedia então, conclui-se, que você não leu ou não sabe. Logo, para não ficar mal visto(a), o melhor é não citar sites (incluindo wikipedia).</p>
