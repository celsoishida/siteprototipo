<h2 class="title">Figuras</h2>

<p>Observações sobre figuras e trabalhos acadêmicos:</p>
<ul>
<li>Toda figura deve ser explicada antes de aparecer. É preciso uma descrição detalhada de toda sigla, variável ou parte da figura</li>
<li>Deve-se explicar todos os detalhes para que a figura seja autoexplicativa</li>
<li>Toda figura deve ser referenciada no texto. Caso contrário, deixe no <a href="apendice.php">Apêndice</a></li>
<li>Foi tirado de alguma fonte? Cite a fonte 'Fonte: (Ishida 2013)'. Se modificou algo utilize 'Fonte: Adaptado de Ishida (2013)'</li>
<li>Prefira traduzir do que deixar a figura em inglês</li>
<li>Não deixe tabelas como sendo figuras</li>
<li>Evitar: figura 'abaixo' e 'acima'</li>
<li>Figuras devem ser chamadas de 'Figura X', com a primeira letra maiúscula e o número da mesma</li>
<li>Utilizar 'Inserir Legenda' no MS Word para incluir a referência abaixo da figura e 'Referência cruzada' para citar a mesma</li>
