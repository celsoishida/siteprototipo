<h2 class="title">Apêndice e Anexo</h2>

       
<p>Tanto o apêndice como o anexo complementam a argumentação do texto, mas não fazem parte do texto principal. Por exemplo, se em um experimento o importante é apenas saber o resultado final de um teste secundário (não o principal), dentro do texto pode-se citar este resultado e no Apêndice pode-se colocar os dados detalhados que foram utilizados para o cálculo (o todo ou uma parte).</p>

<br />

<p>A diferença entre os dois é que Anexo não é elaborado pelo autor, ao contrário do Apêndice que é do próprio autor.</p>


