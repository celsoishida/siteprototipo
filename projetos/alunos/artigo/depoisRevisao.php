<h2 class="title">Depois da revisão</h2>

<p>Depois que fizer todas as alterações faça uma revisão geral:</p>
<ul>
<li>Revise todas as referências (utilizadas estão presentes no final? E retire as não utilizadas)</li>
<li>Reveja a sequência das ideias</li>
<li>Revise o português</li>
<li>Reveja a formatação (parágrafos, figuras, tabelas)</li>
</ul>


<br />
<p>O texto tem que facilitar a 'fluidez' de leitura. Para tornar o texto agradável, um dos primeiros passos é saber que as ideias de cada parágrafo estão concatenadas. Que existe uma sequência e lógica das ideias; e que as ideias levem ao entendimentos dos resultados de uma pesquisa que o interessa.</p>
<br />
<p>O resumo deve conter o que tem no artigo; o conteúdo sempre é alterado a cada revisão, logo vale a pena rever o resumo.</p>
<br />
<p>Tem que haver coerência das informações. O objetivo que está no resumo deve ser o mesmo que tem na introdução e deve ser relembrado na conclusão do artigo. Logo, vale a pena verificar novamente estas informações.</p>
