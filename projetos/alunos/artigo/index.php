<h2 class="title">Observações</h2>

Observação: <a href="http://www.cedsmi.ufpr.br/celsoishida/pesquisa/">dissertações de mestrado e teses de doutorados</a>, geralmente, possuem referência a textos que podem ser interessantes para graduandos que estão começando a conhecer uma área. <a href="http://www.cedsmi.ufpr.br/celsoishida/pesquisa/">Encontre os trabalhos por aqui</a>.


<p>Os observações sobre a correção dos trabalhos são devolvidos em forma de comentários e com o controle de alteração do MS Word. As cores no documento segue os padrões:</p>
<br />
<dl> 
<dd><img src="imagens\corComplementarTexto.png">Complemente o texto</dd>
<dd><img src="imagens\corMelhorarTexto.png">Melhore o que está escrito e revise o texto</dd>
<dd><img src="imagens\corReescreverTexto.png">Reescreva o texto (só mantenha a mesma ideia). Depois revise o parágrafo anterior e posterior</dd>
</dl>
<br />
<h3>Observações</h3>
<ul>
<li>Algumas frases são acrescentadas pelo revisor e estão destacadas com uma cor. O que fazer: o autor do texto deve revisar o texto considerando as ideias escritas</li>
<li>Depois de cada alteração, aconselha-se a ler o parágrafo anterior e posterior</li>
<li>Depois da revisão do professor, o aluno deve fazer as correções do que se pede nos comentários e salvar num novo documento. Os comentários que foram feitos conforme o pedido do professor devem ser excluídos, outros comentários devem ser mantidos e respondidos para discussão. Novas inclusões e modificações devem ser feitos com o 'Controle de alterações' ligado</li>
</ul>

