// Declaração das variáveis numéricas
const numero1 = 10;
const numero2 = 5;

// Cálculos
const soma = numero1 + numero2;
const subtracao = numero1 - numero2;
const multiplicacao = numero1 * numero2;
const divisao = numero1 / numero2;

// Exibição dos resultados no console
console.log('=== Cálculos Matemáticos === usando Template literals');
console.log(`Números: ${numero1} e ${numero2}\n`);
console.log(`Soma: ${numero1} + ${numero2} = ${soma}`);
console.log(`Subtração: ${numero1} - ${numero2} = ${subtracao}`);
console.log(`Multiplicação: ${numero1} * ${numero2} = ${multiplicacao}`);
console.log(`Divisão: ${numero1} / ${numero2} = ${divisao}`);

console.log('=== Cálculos Matemáticos === usando Concatenação');
console.log('Números: ' + numero1 + ' e ' + numero2 + '\n');
console.log('Soma: ' + numero1 + ' + ' + numero2 + ' = ' + soma);
console.log('Subtração: ' + numero1 + ' - ' + numero2 + ' = ' + subtracao);
console.log('Multiplicação: ' + numero1 + ' * ' + numero2 + ' = ' + multiplicacao);
console.log('Divisão: ' + numero1 + ' / ' + numero2 + ' = ' + divisao);

